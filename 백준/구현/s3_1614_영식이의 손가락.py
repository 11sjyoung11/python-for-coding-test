# 다친 손가락 번호 입력 (1=엄지, 5=새끼)
f = int(input())

# 다친 손가락으로 셀 수 있는 최대 횟수 입력
n = int(input())

# 0번도 못 쓰면 시작 불가
if n == 0:
    print(0)
    exit()

# 손가락을 세는 순서 (왕복 1→5→1 형태)
# 한 주기(8번)마다 이 순서가 반복됨
pattern = [1, 2, 3, 4, 5, 4, 3, 2]

idx = 0        # 현재 손가락 위치 인덱스
count = 0      # 전체 센 횟수 (출력용)
limit = n      # 다친 손가락으로 셀 수 있는 남은 횟수
answer = 0     # 실제로 센 숫자의 개수

# 손가락을 차례로 세면서 시뮬레이션
while limit > 0:
    # 현재 손가락이 다친 손가락이면 카운트 감소
    if pattern[idx] == f:
        limit -= 1
        # 마지막으로 다친 손가락을 쓴 순간 멈춤
        if limit == 0:
            break
    # 한 번 센 것으로 처리
    answer += 1
    # 다음 손가락으로 이동 (8개 주기 반복)
    idx = (idx + 1) % 8

# 실제 숫자는 1부터 세므로 +1
print(answer + 1)