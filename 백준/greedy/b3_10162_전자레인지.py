T = int(input()) 

# 버튼 A: 300초(5분), 버튼 B: 60초(1분), 버튼 C: 10초

# A 버튼 최대
A = T // 300      # 300으로 나눈 몫 = A 버튼 누른 횟수
T %= 300          # 남은 시간 (300으로 나눈 나머지)

# B 버튼 최대
B = T // 60       # 60으로 나눈 몫 = B 버튼 누른 횟수
T %= 60           # 남은 시간 (60으로 나눈 나머지)

# C 버튼 최대
C = T // 10       # 10으로 나눈 몫 = C 버튼 누른 횟수
T %= 10           # 남은 시간 (10으로 나눈 나머지)

# 만약 남은 시간이 0이 아니라면 10초 단위로도 나눠떨어지지 않음
if T != 0:
    print(-1)     # 정확히 맞출 수 없으므로 -1 출력
else:
    print(A, B, C)  # 각 버튼을 누른 횟수 출력
